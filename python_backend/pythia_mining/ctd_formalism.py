def _require_numpy() -> Any:
    if np is None:
        raise ModuleNotFoundError("numpy is required for Hilbert-space numeric operations")
    return np


@dataclass(frozen=True, init=False)
class HilbertState:
    """A normalized finite-dimensional complex Hilbert-space state."""

    amplitudes: np.ndarray

    def __init__(
        self, amplitudes: Sequence[complex] | None = None, *, vector: Sequence[complex] | None = None
    ) -> None:
        source = amplitudes if amplitudes is not None else vector
        if source is None:
            raise ValueError("Hilbert state requires amplitudes or vector")
        normalized = self._normalize(source)
        object.__setattr__(self, "amplitudes", normalized)

    @staticmethod
    def _normalize(amplitudes: Sequence[complex]) -> Any:
        numpy = _require_numpy()
        values = numpy.asarray(amplitudes, dtype=numpy.complex128).reshape(-1)
        if values.size == 0:
            raise ValueError("Hilbert state must contain at least one amplitude")
        norm = float(numpy.linalg.norm(values))
        if not math.isfinite(norm) or norm <= 0.0:
            raise ValueError("Hilbert state norm must be finite and positive")
        return values / norm

    @classmethod
    def from_amplitudes(cls, amplitudes: Sequence[complex]) -> "HilbertState":
        return cls(amplitudes)

    @property
    def dimension(self) -> int:
        return int(self.amplitudes.size)

    def density_matrix(self) -> np.ndarray:
        psi = self.amplitudes.reshape(-1, 1)
        return psi @ psi.conj().T

    def trace(self) -> float:
        numpy = _require_numpy()
        return float(numpy.trace(self.density_matrix()).real)

    def eigenvalues(self) -> Any:
        numpy = _require_numpy()
        return numpy.linalg.eigvalsh(self.density_matrix()).real

    def born_probabilities(self) -> np.ndarray:
        numpy = _require_numpy()
        probabilities = numpy.abs(self.amplitudes) ** 2
        return probabilities / max(float(numpy.sum(probabilities)), numpy.finfo(float).tiny)

    def evolve(self, unitary: np.ndarray, *, atol: float = 1e-9) -> "HilbertState":
        numpy = _require_numpy()
        matrix = numpy.asarray(unitary, dtype=numpy.complex128)
        if matrix.shape != (self.dimension, self.dimension):
            raise ValueError(
                f"unitary shape {matrix.shape!r} does not match state dimension {self.dimension}"
            )
        identity = numpy.eye(self.dimension, dtype=numpy.complex128)
        if not numpy.allclose(matrix.conj().T @ matrix, identity, atol=atol):
            raise ValueError("evolution operator must be unitary")
        return HilbertState.from_amplitudes(matrix @ self.amplitudes)


def compute_phi_resonance(nonce: int, harmonics: Sequence[int] = (1, 2, 3, 5, 8, 13, 21)) -> float:
    """Compute bounded multi-harmonic Φ-resonance for a nonce-like integer.

    The score is a formalism feature, not evidence that Bitcoin nonces are
    causally generated by Φ. Multiple Fibonacci harmonics are used to avoid
    overfitting to one phase window while retaining a deterministic [0, 1] score.
    """

    value = int(nonce) & 0xFFFFFFFF
    best = 0.0
    for harmonic in harmonics:
        phase = (value * (PHI ** int(harmonic))) % 1.0
        best = max(best, 1.0 - 2.0 * abs(phase - 0.5))
    return float(max(0.0, min(1.0, best)))


@dataclass(frozen=True)
class BuresManifold:
    """Minimal finite-dimensional Bures manifold stability model."""

    dimension: int

    def __post_init__(self) -> None:
        if int(self.dimension) < 2:
            raise ValueError("dimension must be >= 2")

    def compute_ricci_scalar(self) -> float:
        dim = int(self.dimension)
        return float((dim - 1) / dim)


@dataclass(frozen=True)
class CTDClaimReview:
    """Explicit review boundary for what this software implementation proves."""

    implements_hilbert_space_math: bool
    implements_born_rule: bool
    implements_unitary_evolution: bool
    uses_pulvini_phi_compression: bool
    substrate_agnostic_math: bool
    claims_physical_quantum_hardware: bool
    claims_universal_quantum_speedup: bool
    claims_rsa_break: bool
    claims_physical_quantum_computers_unnecessary: bool

    @property
    def rigorous(self) -> bool:
        return (
            self.implements_hilbert_space_math
            and self.implements_born_rule
            and self.implements_unitary_evolution
            and self.uses_pulvini_phi_compression
            and self.substrate_agnostic_math
            and not self.claims_physical_quantum_hardware
            and not self.claims_universal_quantum_speedup
            and not self.claims_rsa_break
            and not self.claims_physical_quantum_computers_unnecessary
        )

    def as_dict(self) -> dict[str, bool]:
        data = asdict(self)
        data["rigorous"] = self.rigorous
        return data


@dataclass(frozen=True)
class DeutschPulviniBenchmarkRow:
    """Complexity row comparing dense states with structured PULVINI/MPS states."""

    qubits: int
