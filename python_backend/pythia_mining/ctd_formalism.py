"""Church-Turing-Deutsch formalism helpers for substrate-agnostic math.

This module implements a deliberately bounded interpretation of the CTD
position paper used by HYBA/PYTHIA: Hilbert-space mathematics can be executed on
ordinary software substrates, while claims about physical quantum hardware or
asymptotic quantum speedup remain explicit non-claims unless separately proven.

The implementation focuses on auditable invariants:
  * normalized complex state vectors in finite Hilbert spaces,
  * density matrices and Born-rule probabilities,
  * unitary evolution with norm preservation,
  * PULVINI/φ working-set compression with retained reconstruction kernels, and
  * a claim-boundary review packet separating implemented mathematics from
    unproven physical/computational claims.
"""

